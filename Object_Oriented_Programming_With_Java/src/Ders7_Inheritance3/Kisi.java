package Ders7_Inheritance3;

public class Kisi {

	private String name;
	private String soyadi;
	
	public Kisi(String name, String soyadi) {
		super();
		this.name = name;
		this.soyadi = soyadi;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSoyadi() {
		return soyadi;
	}

	public void setSoyadi(String soyadi) {
		this.soyadi = soyadi;
	}
	
	
	
}
